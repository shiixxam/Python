# 8.	Bitwise OR: Request two integers from the user and use the bitwise OR operator ( | ) to print the result of their bitwise OR operation.




num1 = int(input("Enter the first integer: "))
num2 = int(input("Enter the second integer: "))

result = num1 | num2                             #or symbol |

# Print the result
print("Bitwise OR result:", result)
