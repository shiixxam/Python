# 5.	Number Sign (Ternary): Request a number from the user and use the ternary operator to print "Positive" if the number is greater than zero, "Negative" if it's less than zero, and "Zero" if it's equal to zero.

# Input a number from the user
number = float(input("Enter a number: "))
if number > 0:
    print("positive")
 
else:
      number < 0
      print("negative")
      

