# 5.	Countdown with Break: Use a while loop to count down from 10 to 1, but exit the loop if the countdown reaches 5 using the break statement.



countdown = 10

while countdown >= 1:
    if countdown == 5:
        break  
    print(countdown)
    countdown -= 1


count = 10

while count >= 1:
    if count == 5:
        break  
    print(count)
    count -= 2

