# 3.	Temperature Converter: Ask the user for a temperature in Celsius using input() , convert it to Fahrenheit, and print the result.





celsius = float(input("Enter a temperature in Celsius: "))


fahrenheit = (celsius * 9/5) + 32   #    (user input) *9/5 +32 is the fromula to calculator the temperature.


print(f"{celsius} degrees Celsius is equal to {fahrenheit} degrees Fahrenheit.")






