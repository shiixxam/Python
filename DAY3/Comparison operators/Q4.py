# 4.	Equality Check with Strings: Prompt the user to enter two words as strings. Use the equality operator (==) to check if the words are the same and print the result.

word1 = input("Enter the first word: ")
word2 = input("Enter the second word: ")

if word1 == word2:
    print("The words are the same.")
else:
    print("The words are not the same.")
