# 5.	Inequality Check with Numbers: Request two numeric values from the user and use the inequality operator (!=) to determine if they are different. Print the result.


num1 = float(input("Enter the first numeric value: "))
num2 = float(input("Enter the second numeric value: "))

if num1 != num2:
    print("The numeric values are different.")
else:
    print("The numeric values are the same.")
